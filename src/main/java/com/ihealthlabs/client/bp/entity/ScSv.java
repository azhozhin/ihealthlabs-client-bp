package com.ihealthlabs.client.bp.entity;

public class ScSv {
    private String sc;
    private String sv;

    public ScSv(String sc, String sv) {
        this.sc = sc;
        this.sv = sv;
    }

    public String getSc() {
        return sc;
    }

    public void setSc(String sc) {
        this.sc = sc;
    }

    public String getSv() {
        return sv;
    }

    public void setSv(String sv) {
        this.sv = sv;
    }
}
